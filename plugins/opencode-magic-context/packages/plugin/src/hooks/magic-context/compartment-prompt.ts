/** Build the per-request prompt for a compressor run.
 *  `outputDepth` selects the density level (1-4). Depth 5 is handled by
 *  the runner short-circuit and never invokes this builder. */
export function buildCompressorPrompt(
    compartments: Array<{
        startMessage: number;
        endMessage: number;
        title: string;
        content: string;
    }>,
    currentTokens: number,
    targetTokens: number,
    outputDepth: number,
    outputCount?: number,
): string {
    const lines: string[] = [];
    const densityLabel =
        outputDepth === 1
            ? "仅合并"
            : outputDepth === 2
              ? "轻度精简"
              : outputDepth === 3
                ? "完全压缩"
                : "极限电报";
    const resolvedOutputCount = outputCount ?? Math.max(1, Math.ceil(compartments.length / 2));

    lines.push(
        `密度目标：级别 ${outputDepth}（${densityLabel}）。级别规则参见系统提示。`,
    );
    lines.push(
        `输入：${compartments.length} 个压缩区，约 ${currentTokens} tokens。目标输出：正好 ${resolvedOutputCount} 个压缩区，总计约 ${targetTokens} tokens。`,
    );
    lines.push("");

    // Depth-specific reminder (keep it brief; full rules live in system prompt).
    if (outputDepth === 1) {
        lines.push(
            "仅合并。保留叙述和所有 U: 行。仅移除跨越压缩区的真正重复句子。",
        );
    } else if (outputDepth === 2) {
        lines.push("合并 + 丢弃填充词和模糊用语。保持语法，逐字保留 U: 行。");
    } else if (outputDepth === 3) {
        lines.push(
            "合并为单段压缩区。丢弃冠词和弱助动词。只保留不可替代的 U: 行。",
        );
    } else {
        lines.push(
            "合并为电报式片段，使用符号连接（→ + // |）。仅在真正不可替代时保留 U: 行。",
        );
    }
    lines.push("");
    lines.push("在所有级别逐字保留：提交哈希、文件路径、URL、代码片段。");
    lines.push("");

    for (const c of compartments) {
        lines.push(
            `<compartment start="${c.startMessage}" end="${c.endMessage}" title="${escapeXmlAttr(c.title)}">`,
        );
        lines.push(escapeXmlContent(c.content));
        lines.push("</compartment>");
        lines.push("");
    }
    lines.push("返回已合并的压缩区作为 XML。");
    return lines.join("\n");
}

